package com.leppy.redux.util;

public class RGBFWrapper {
    public final float R;
    public final float G;
    public final float B;
    public final float F;

    public RGBFWrapper(float r, float g, float b, float f) {
        this.R = r;
        this.G = g;
        this.B = b;
        this.F = f;
    }
}
